#include "libft.h"

int		ft_atoip(char **str)
{
	int		nb;
	char	*s;

	nb = 0;
	s = *str + (**str == '-');
	while (*s > 47 && *s < 58)
		nb = nb * 10 + *s++ - '0';
	if (**str == '-')
		nb *= -1;
	*str = s;
	return (nb);
}
